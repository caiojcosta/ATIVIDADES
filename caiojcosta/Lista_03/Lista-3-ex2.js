/*  (OBRIGATÓRIO) Criar um algoritmo que realize a multiplicação de dois números maiores que zero SEM
usar o operador de multiplicação (*) */

var user = require("readline-sync");

var n1 = user.questionInt("Digite um numero: ");
var n2 = user.questionInt("Digite outro numero: ");
var cont = 0;

var soma = 0;

while (n1 > cont) {
    soma = n2 + soma
    cont++
}

console.log(soma);